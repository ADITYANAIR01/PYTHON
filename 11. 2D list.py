# 2D List is a list of lists

drinks  = ["Coffee", "Tea", "Coca Cola"] #index 0
dishes   = ["Pizza", "Burger", "Chicken"]  #index 1
dessert = ["Ice cream", "Chocolate pudding", "Cake"] #index 2 

# To combine multiple list use variable_name = [list1,list2,list3]
food = [drinks, dishes, dessert]

# To acess a index & item on the list use print(name[][])
print(food[1][1])
